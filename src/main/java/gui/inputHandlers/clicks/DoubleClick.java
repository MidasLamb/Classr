package gui.inputHandlers.clicks;

/**
 * Class that represents a MouseClick that is a double click.
 */
public class DoubleClick extends MouseClick {

	/**
	 * Create a new DoubleClick and set its coordinates.
	 * 
	 * @param x
	 *            x-coordinate
	 * @param y
	 *            y-coordinate
	 */
	public DoubleClick(int x, int y) {
		super(x, y);
	}

}
